-- ************************************************************
-- 1. 기본 테이블 생성
-- ************************************************************

CREATE TABLE IF NOT EXISTS treatments (
  id SERIAL PRIMARY KEY,
  name TEXT NOT NULL UNIQUE,
  category TEXT NOT NULL,
  spec TEXT,
  description TEXT,
  concerns TEXT[],
  interval_weeks INT,
  distance INT,
  effect TEXT,
  pain INT,
  downtime TEXT,
  duration TEXT,
  sessions TEXT,
  good TEXT[],
  avoid TEXT[],
  aftercare TEXT,
  avoid_act TEXT,
  caution TEXT,
  price TEXT,
  price_range TEXT,
  event_dose TEXT,
  effective_dose TEXT,
  tips JSONB
);

CREATE TABLE IF NOT EXISTS categories (
  key TEXT PRIMARY KEY,
  name TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS hospitals (
  id SERIAL PRIMARY KEY,
  name TEXT NOT NULL,
  area TEXT,
  address TEXT,
  phone TEXT
);

CREATE TABLE IF NOT EXISTS concern_data (
  concern TEXT PRIMARY KEY,
  primary_treatments TEXT[],
  secondary_treatments TEXT[],
  description TEXT
);

CREATE TABLE IF NOT EXISTS botox_brands (
  id SERIAL PRIMARY KEY,
  name TEXT NOT NULL,
  maker TEXT,
  description TEXT
);

CREATE TABLE IF NOT EXISTS reviews (
  id SERIAL PRIMARY KEY,
  hospital_id INT REFERENCES hospitals(id),
  user_id UUID REFERENCES auth.users(id),
  treatment TEXT,
  rating INT CHECK (rating BETWEEN 1 AND 5),
  text TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS user_records (
  id SERIAL PRIMARY KEY,
  user_id UUID REFERENCES auth.users(id),
  treatment_name TEXT NOT NULL,
  date DATE NOT NULL,
  memo TEXT,
  photo_url TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS user_routines (
  id SERIAL PRIMARY KEY,
  user_id UUID REFERENCES auth.users(id) UNIQUE,
  routine JSONB,
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS dosage_guide (
  treatment_name TEXT PRIMARY KEY REFERENCES treatments(name),
  event TEXT NOT NULL,
  recommended TEXT NOT NULL,
  unit TEXT NOT NULL
);


-- ************************************************************
-- 2. 추가 테이블 (데일리케어, 분석기록 등)
-- ************************************************************

CREATE TABLE IF NOT EXISTS daily_completed (
  id bigint generated always as identity primary key,
  uid text not null,
  date_key text not null,
  task_id text not null,
  created_at timestamptz default now(),
  unique(uid, date_key, task_id)
);

CREATE TABLE IF NOT EXISTS daily_custom_tasks (
  id bigint generated always as identity primary key,
  uid text not null,
  date_key text not null,
  task_time text not null,
  task_text text not null,
  task_icon text not null default '✨',
  task_id text not null,
  created_at timestamptz default now(),
  unique(uid, task_id)
);

CREATE TABLE IF NOT EXISTS daily_routines (
  id bigint generated always as identity primary key,
  uid text not null unique,
  routine jsonb not null default '[]',
  updated_at timestamptz default now()
);

CREATE TABLE IF NOT EXISTS daily_photos (
  id bigint generated always as identity primary key,
  uid text not null,
  date_key text not null,
  photo_url text not null,
  created_at timestamptz default now(),
  unique(uid, date_key)
);

CREATE TABLE IF NOT EXISTS treatment_records (
  id bigint generated always as identity primary key,
  uid text not null unique,
  step text not null default 'select',
  counts jsonb not null default '{}',
  start_date text,
  schedule jsonb not null default '[]',
  past_treatments jsonb not null default '[]',
  updated_at timestamptz default now()
);

CREATE TABLE IF NOT EXISTS analysis_records (
  id bigint generated always as identity primary key,
  uid text not null,
  photo_url text,
  result jsonb not null default '{}',
  created_at timestamptz default now()
);

